package entity;

public class Picture {
	
	private int recipe;
	private int number;
	private String url;
	
	public void setRecipe(int recipe) {
		this.recipe=recipe;
	}
	public int getRecipe() {
		return this.recipe;
	}
	
	public void setNumber(int number) {
		this.number=number;
	}
	public int getNumber() {
		return this.number;
	}
	
	public void setUrl(String url) {
		this.url=url;
	}
	public String getUrl() {
		return this.url;
	}

}
